La deforestazione in Kenya ha assunto nel tempo dimensioni tali da accelerare il processo di degrado dell'ambiente (perdita della fertilità del terreno, inondazioni, insabbiamento, scomparsa di habitat importanti per la varietà biologica delle specie presenti) con conseguenti ripercussioni negative sulle condizioni di vita della popolazione. Tale degrado è causato dalla povertà e dall’incremento demografico. Le risorse forestali (foreste naturali e rimboschimenti) sono oggetto di tagli abusivi ed anche minacciate di distruzione a causa degli incendi boschivi e del pascolo intensivo. Il progetto aveva come obiettivo principale la promozione di una gestione sostenibile della foresta naturale nell'area di Baringo, la creazione di nuove risorse forestali mediante lo sviluppo di sistemi agro-silvo-pastorali, la piantagione di specie multi-uso, la promozione delle capacità gestionali mediante l'organizzazione di corsi di formazione per il personale locale.

